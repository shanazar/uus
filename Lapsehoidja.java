public class Lapsehoidja {

    private int firmaNumber;
    private String lapseHoidja;

    public Lapsehoidja(int firmaNumber, String lapseHoidja) {
        this.firmaNumber = firmaNumber;
        this.lapseHoidja = lapseHoidja;
    }

    public int getFirmaNumber() {
        return firmaNumber;
    }

    public void setFirmaNumber(int firmaNumber) {
        this.firmaNumber = firmaNumber;
    }

    public String getLapseHoidja() {
        return lapseHoidja;
    }

    public void setLapseHoidja(String lapseHoidja) {
        this.lapseHoidja = lapseHoidja;
    }
}
